    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
String identificacion = txtidentificacion.getText();
String nombre = txtnombre.getText();
String fechaNacimiento = txtfechanacimiento.getText();  // Aseg√∫rate de obtener correctamente la fecha de nacimiento
String telefono = txttelefono.getText();
String correo = txtcorreo.getText();

Clientes cliente = new Clientes(identificacion, nombre, fechaNacimiento, telefono, correo);

clientesController.insert(cliente);


txtidentificacion.setText("");
txtnombre.setText("");
txtfechanacimiento.setText("");
txttelefono.setText("");
txtcorreo.setText("");
    }//GEN-LAST:event_btnGuardarActionPerformed

